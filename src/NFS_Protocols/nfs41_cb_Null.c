/*
 * Call the NFSv41 client's CB_NULL procedure.
 */
enum clnt_stat
rpc_cb_null(CLIENT *clnt)
{
    struct timeval CB_TIMEOUT = {15, 0};

    return (clnt_call(clnt, CB_NULL, (xdrproc_t) xdr_void, NULL,
		      (xdrproc_t) xdr_void, NULL, CB_TIMEOUT));
}
